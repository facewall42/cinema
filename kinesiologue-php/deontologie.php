                <h3>Préambule</h3>
                <p>
                    La kinésiologie est une pratique non médicale visant à favoriser un état d’équilibre et de
                    bien-être physique, mental, émotionnel et énergétique. Elle utilise principalement le test
                    musculaire comme outil de base pour identifier et accompagner les déséquilibres liés au
                    stress et aux émotions. Le kinésiologue s’engage à respecter les principes éthiques et
                    déontologiques suivants dans l’exercice de sa profession.
                </p>
                <h3>Article 1 : Champ d’intervention du kinésiologue</h3>
                <p>1.1. Le kinésiologue n’est pas un professionnel de santé. Il n’établit aucun diagnostic
                    médical, ne prescrit pas de traitement, ne traite pas et ne demande jamais d’interrompre un
                    traitement médical.
                </p>
                <p>1.2. La kinésiologie ne se substitue pas à la médecine. Le kinésiologue rappelle à ses
                    clients l’importance de consulter un médecin ou un professionnel de santé en cas de maladie,
                    symptômes physiques ou problèmes de santé.
                </p>
                <p>1.3. Le kinésiologue accompagne ses clients dans la gestion du stress et des émotions, en
                    respectant les limites de sa pratique.
                </p>
                <h3>Article 2 : Respect de la personne</h3>
                <p>2.1. Le kinésiologue s’engage à accueillir toute personne sans discrimination, quels que soient son
                    âge, sa condition, ses convictions politiques, religieuses ou culturelles.</p>
                <p>2.2. Il respecte la dignité, l’intégrité et les choix de chaque client.</p>
                <p>2.3. Le kinésiologue s’abstient de tout prosélytisme politique, religieux ou spirituel.</p>
                <p>2.4. Il veille à ne jamais juger, culpabiliser ou blâmer les personnes qui le consultent.</p>
                <h3>Article 3 : Secret professionnel et confidentialité</h3>
                <p>3.1. Le kinésiologue est tenu au secret professionnel. Toute information confidentielle
                    obtenue dans le cadre de sa pratique ne peut être divulguée, sauf dérogation prévue par la
                    loi.
                </p>
                <p>3.2. Il garantit la confidentialité des données personnelles de ses clients et s’engage à les
                    protéger conformément à la législation en vigueur.
                </p>
                <h3>Article 4 : Limites de la pratique</h3>
                <p>4.1. Le kinésiologue reconnaît les limites de sa pratique. Si les besoins du client dépassent
                    ses compétences, il l’oriente vers un professionnel de santé ou un autre praticien qualifié.
                </p>
                <p>4.2. Il s’abstient de mélanger la kinésiologie avec des pratiques ésotériques ou non validées
                    dans le cadre de sa profession.
                </p>
                <p>4.3. Le kinésiologue n’utilise pas le test musculaire pour établir un diagnostic, prédire
                    l’avenir, affirmer des faits invérifiables ou imposer des choix au client.
                </p>
                <h3>Article 5 : Information et transparence</h3>
                <p>5.1. Le kinésiologue informe clairement ses clients sur les techniques utilisées, notamment
                    si elles diffèrent de la kinésiologie.
                </p>
                <p>5.2. Il s’abstient de présenter la kinésiologie comme une méthode de guérison ou de recourir
                    à un langage médical ou ésotérique dans sa communication.
                </p>
                <p>5.3. Le kinésiologue laisse le client libre de poursuivre ou d’interrompre les séances à tout
                    moment, sans pression ni obligation.
                </p>
                <h3>Article 6 : Formation et compétences</h3>
                <p>6.1. Le kinésiologue s’engage à actualiser régulièrement ses compétences par la formation
                    continue.
                </p>
                <p>6.2. Il fait appel à un superviseur ou à un pair chaque fois que nécessaire pour garantir la
                    qualité de sa pratique.
                </p>
                <p>6.3. Il reconnaît ses limites personnelles et professionnelles et agit avec vigilance dans
                    l’intérêt de ses clients.
                </p>
                <h3>Article 7 : Relations avec les autres professionnels</h3>
                <p>7.1. Le kinésiologue entretient des relations respectueuses et courtoises avec ses confrères
                    et consœurs, ainsi qu’avec les membres des autres professions.
                </p>
                <p>7.2. Il s’abstient de tout propos diffamatoire ou discréditant à l’égard d’autres praticiens
                    ou professions.
                </p>
                <p>7.3. Le kinésiologue ne prétend pas collaborer avec des professionnels de santé (médecins,
                    chirurgiens, podologues, etc.) dans le cadre de sa pratique.
                </p>
                <h3>Article 8 : Responsabilité et manquements</h3>
                <p>8.1. Le kinésiologue est responsable de ses actes et s’engage à respecter ce code de
                    déontologie dans l’exercice de sa profession.
                </p>
                <p>8.2. Tout manquement à ce code peut faire l’objet d’une plainte déposée par un client ou un
                    pair, et entraîner des conséquences disciplinaires ou juridiques.
                </p>
                <p>8.3. Le kinésiologue reconnaît l’autorité du Syndicat National des Kinésiologues (SNK) et de
                    la Fédération Française de Kinésiologie pour modifier et faire évoluer ce code.
                </p>
                <h3>Annexe : Références</h3>
